package form;

public class OrdenadorForm {
	private Integer inputId;
	private String inputNombre;
	private String inputSerial;
	private Integer inputPersona;
	
	public Integer getInputId() {
		return inputId;
	}
	public void setInputId(Integer inputId) {
		this.inputId = inputId;
	}
	public String getInputNombre() {
		return inputNombre;
	}
	public void setInputNombre(String inputNombre) {
		this.inputNombre = inputNombre;
	}
	public String getInputSerial() {
		return inputSerial;
	}
	public void setInputSerial(String inputSerial) {
		this.inputSerial = inputSerial;
	}
	public Integer getInputPersona() {
		return inputPersona;
	}
	public void setInputPersona(Integer inputPersona) {
		this.inputPersona = inputPersona;
	}
}
